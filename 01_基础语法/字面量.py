# 整数
print(111)
# type()可以得到类型
print(type(111))
name = 111
name_type = type(name)
print(name_type,name)

# 浮点数
print(11.111111)
print(type(11.111111))
name = 11.111111
name_type = type(name)
print(name_type,name)

# 字符串
print("hello 李")
print(type("hello 李"))
name = "hello 李"
name_type = type(name)
print(name_type,name)



